package aula0823;

public class OperacoesAritmeticas {
    public static void main(String[] args) {
        int a = 13;
        int b = 12;

        int soma = a + b;
        int multiplicacao = a * b;

        System.out.println("A soma entre "+a+" e "+b+" é "+ soma);
        System.out.println("A multiplicação entre "+a+" e "+b+" é "+ multiplicacao);
    }
}
